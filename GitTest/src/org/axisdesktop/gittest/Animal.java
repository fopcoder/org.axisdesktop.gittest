package org.axisdesktop.gittest;

public class Animal {
	private int legs;
	private String name;
	private double weight;

	public int getLegs() {
		return legs;
	}

	public void setLegs( int legs ) {
		this.legs = legs;
	}

	public String getName() {
		return name;
	}

	public void setName( String name ) {
		this.name = name;
	}

	public double getWeight() {
		return weight;
	}

	public void setWeight( double weight ) {
		this.weight = weight;
	}

}
